    private void txtNoregKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNoregKeyPressed
        noreg=txtNoreg.getText();
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try {
                sql="SELECT * FROM tabel_pasien " + "where no_reg='"+ noreg +"'";
                st=con.createStatement();
                RsPasien=st.executeQuery(sql);
                while (RsPasien.next()) {
                   txtNama.setText(RsPasien.getString("nama_pasien"));
                   txtAlamat.setText(RsPasien.getString("alamat_pasien"));
                   txtTempat.setText(RsPasien.getString("tempat_lahir"));
                   jDlahir.setDate(RsPasien.getDate("tanggal_lahir"));
                   cmbKelamin.setSelectedItem(RsPasien.getString("jenis_kelamin"));
                   txtHp.setText(RsPasien.getString("no_hp"));
                   JOptionPane.showMessageDialog(null, "Data Ditemukan");
                   butTambah.setEnabled(false);
                   butSimpan.setEnabled(false);
                   butHapus.setEnabled(true);
                   txtNoreg.setEnabled(false);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan \n"+e.getMessage());
                txtNoreg.requestFocus();
            }
        }
    }//GEN-LAST:event_txtNoregKeyPressed

