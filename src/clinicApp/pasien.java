    private void butCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCariActionPerformed
        statusSearcing = 1;
        if(txtCari.getText().isEmpty())
            {statusSearcing = 0;}
        else if(statusSearcing==1){
            DefaultTableModel datalist = new DefaultTableModel();
            datalist.addColumn("No. Reg");
            datalist.addColumn("Nama");
            datalist.addColumn("Alamat");
            datalist.addColumn("Tempat Lahir");
            datalist.addColumn("Tanggal Lahir");
            datalist.addColumn("Jenis Kelamin");
            datalist.addColumn("No. HP");
            
            String cari = txtCari.getText();
       try{
            String sql = ("SELECT * FROM tabel_pasien WHERE no_reg LIKE '"+cari+"%'"
                    + "OR nama_pasien like '"+cari+"%'"
                    + "OR no_hp like '"+cari+"%' ORDER BY no_reg");
            st=con.createStatement();
            RsPasien = st.executeQuery(sql);
            while (RsPasien.next()){
                datalist.addRow(new Object[]{
                    RsPasien.getString(1), RsPasien.getString(2),RsPasien.getString(3),
                    RsPasien.getString(4), RsPasien.getString(5),RsPasien.getString(6),
                    RsPasien.getString(7)
                });
            }
            gridPasien.setModel(datalist);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data BELUM TERDAFTAR\n"+e.getMessage());
        } 
        }
    }//GEN-LAST:event_butCariActionPerformed

