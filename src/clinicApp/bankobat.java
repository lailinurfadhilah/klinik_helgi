    private void showdata(String sql){
        DefaultTableModel datalist = new DefaultTableModel();
        datalist.addColumn("Kode Obat");
        datalist.addColumn("Nama Obat");
        datalist.addColumn("Jenis Obat");
        datalist.addColumn("Harga Obat");
        try {
            st=con.createStatement();
            RsObat=st.executeQuery("SELECT * FROM `tabel_obat`");
            while (RsObat.next()){
                datalist.addRow(new Object[]{
                    RsObat.getString(1), RsObat.getString(2), 
                    RsObat.getString(3), RsObat.getString(4)
                });
                gridObat.setModel(datalist);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Obat GAGAL DITAMPILKAN \n"+e.getMessage());
        }
    }

