    private void txtKodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKodeKeyPressed
        kode=txtKode.getText();
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try {
                sql="SELECT * FROM tabel_obat where kode_obat='"+ kode +"'";
                st=con.createStatement();
                RsObat=st.executeQuery(sql);
                while (RsObat.next()) {
                    txtNama.setText(RsObat.getString("nama_obat"));
                    txtJenis.setText(RsObat.getString("jenis_obat"));
                    txtHarga.setText(RsObat.getString("harga_obat"));
                    JOptionPane.showMessageDialog(null,"Data Ditemukan");
                    butTambah.setEnabled(false);
                    butSimpan.setEnabled(false);
                    butHapus.setEnabled(true);
                    
                    txtKode.setEnabled(false);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data TIDAK DITEMUKAN \n"+e.getMessage());
                txtKode.requestFocus();
            }
        }
    }//GEN-LAST:event_txtKodeKeyPressed

    private void butSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSimpanActionPerformed
        kode=String.valueOf(txtKode.getText());
        nama=String.valueOf(txtNama.getText());
        jenis=String.valueOf(txtJenis.getText());
        harga=Integer.parseInt(txtHarga.getText());

        try {
            sql="INSERT INTO tabel_obat(kode_obat, "
            + "nama_obat, "
            + "jenis_obat, "
            + "harga_obat)VALUES"
            + "('"+ kode +"',"
            + "'"+ nama +"',"
            + "'"+ jenis +"',"
            + "'"+ harga +"')";
            st=con.createStatement();
            st.execute(sql);
            showdata("SELECT * FROM tabel_obat");
            form_awal();
            JOptionPane.showMessageDialog(null,"Data BERHASIL DISIMPAN");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data GAGAL DISIMPAN\n"+e.getMessage());
        }
    }//GEN-LAST:event_butSimpanActionPerformed

