    private void showdata (String sql){
        DefaultTableModel datalist = new DefaultTableModel();;
        datalist.addColumn("Poli");
        datalist.addColumn("Nama Dokter");
        datalist.addColumn("Alamat Dokter");
        datalist.addColumn("No. HP");
        datalist.addColumn("Jasa Dokter");
        try {
            st=con.createStatement();
            RsDokter=st.executeQuery("SELECT * FROM tabel_dokter");
            while (RsDokter.next()){
                datalist.addRow(new Object[]{
                    RsDokter.getString(1), RsDokter.getString(2),RsDokter.getString(3), 
                    RsDokter.getString(4), RsDokter.getString(5)
                });
                gridDokter.setModel(datalist);
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data GAGAL DITAMPILKAN\n"+e.getMessage());
        }
        }

