    private void butSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSimpanActionPerformed
        poli=cmbPoli.getItemAt(cmbPoli.getSelectedIndex()).toString();;
        nama=String.valueOf(txtNama.getText());
        alamat=String.valueOf(txtAlamat.getText());
        nohp=String.valueOf(txtHp.getText());
        jasa=String.valueOf(txtJasa.getText());
        try {
            sql="INSERT INTO tabel_dokter(nama_poli, "
            + "nama_dokter, "
            + "alamat_dokter, "
            + "no_hp, "
            + "jasa_dokter)VALUES"
            + "('"+ poli +"',"
            + "'"+ nama +"',"
            + "'"+ alamat +"',"
            + "'"+ nohp +"',"
            + "'"+ jasa +"')";
            st=con.createStatement();
            st.execute(sql);
            showdata("SELECT * FROM tabel_dokter");
            form_awal();
            JOptionPane.showMessageDialog(null, "Data Dokter BERHASIL DISIMPAN");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Dokter GAGAL DISIMPAN \n"+e.getMessage());
        }
    }//GEN-LAST:event_butSimpanActionPerformed

