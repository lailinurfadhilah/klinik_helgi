    private void txtNamaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNamaKeyPressed
        nama=txtNama.getText();
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try {
                sql="SELECT * FROM tabel_dokter WHERE no_dokter='"+ nama +"'";
                st=con.createStatement();
                RsDokter=st.executeQuery(sql);
                while (RsDokter.next()) {
                   cmbPoli.setSelectedItem(RsDokter.getString("nama_poli"));
                   txtNama.setText(RsDokter.getString("nama_pasien"));
                   txtAlamat.setText(RsDokter.getString("alamat_pasien"));
                   txtHp.setText(RsDokter.getString("no_hp"));
                   txtJasa.setText(RsDokter.getString("jasa_dokter"));
                   JOptionPane.showMessageDialog(null, "Data Ditemukan");
                   butTambah.setEnabled(false);
                   butSimpan.setEnabled(false);
                   butHapus.setEnabled(true);
                   txtNama.setEnabled(false);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data TIDAK DITEMUKAN \n"+e.getMessage());
                txtNama.requestFocus();
            }
        }
    }//GEN-LAST:event_txtNamaKeyPressed

